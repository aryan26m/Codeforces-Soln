class Solution {
public:
    int singleNumber(vector<int>& nums) {        
        //your code goes here 
        int n=nums.size();
        int ans=0;
        for(int i=0;i<32;i++){
            int sum=0;
            for(int j=0;j<n;j++){
                if ((nums[j]>>i)&1)
                {
                    sum++;
                }
            }
            if(sum%3){

                ans|=(1<<i);
            }
        }
        return ans;
    }
};